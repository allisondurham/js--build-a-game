import Turf from "./turf.js"

export function buildLevel(game, level) {
    let terrain = [];

    level.forEach((row, rowIndex) => {
        row.forEach((turf, turfIndex) => {
            if (turf === 1) {
                let position = {
                    x: 20 * turfIndex,
                    y: 200 + 20 * rowIndex
                }
                terrain.push(new Turf(game, position));
            }
        });
    });
    return terrain;
}

